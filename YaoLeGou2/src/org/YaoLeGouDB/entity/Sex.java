package org.YaoLeGouDB.entity;

public class Sex {

	private int sexid;
	private String sexname;
	private int count;
	
	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getSexid() {
		return sexid;
	}
	public void setSexid(int sexid) {
		this.sexid = sexid;
	}
	public String getSexname() {
		return sexname;
	}
	public void setSexname(String sexname) {
		this.sexname = sexname;
	}
	
}
